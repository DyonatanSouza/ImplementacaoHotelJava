    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        defineConsulta();
        Cliente cliente = new Cliente();
        ClienteDao dao = new ClienteDao();

        if (txtNome.getText().length() < 5 || txtNome.getText().length() > 50) {
            txtNome.requestFocus();
            jLabel2.setVisible(true);
            txtNome.setText(mensagem1);
        }

        if (txtCpf.getText().length() > 11 || txtCpf.getText().length() < 11) {
            txtCpf.requestFocus();
            jLabel3.setVisible(true);
            txtCpf.setText(mensagem2);
        }
        if (txtData.getText().length() == 0) {
            txtData.requestFocus();
            jLabel5.setVisible(true);
            txtData.setText(mensagem4);
        }
        if (txtEndereco.getText().length() == 0) {
            txtEndereco.requestFocus();
            jLabel6.setVisible(true);
            txtEndereco.setText(mensagem5);
        }
        if (txtN.getText().length() == 0) {
            jLabel7.setVisible(true);
            txtN.setText(mensagem6);
        }
        if (txtCidade.getText().length() == 0) {
            txtCidade.requestFocus();
            jLabel8.setVisible(true);
            txtCidade.setText(mensagem7);
        }
        if (txtUf.getText().length() == 0) {
            txtUf.requestFocus();
            jLabel9.setVisible(true);
            txtUf.setText(mensagem8);
        }

        if ((txtUf.getText().length() > 0) && (txtCidade.getText().length() > 0) && (txtN.getText().length() > 0) && (txtEndereco.getText().length() > 0)
                && (txtData.getText().length() > 0) && (txtTelefone.getText().length() >= 8) && (txtCpf.getText().length() == 11) && (txtNome.getText().length() < 50) && (txtNome.getText().length() > 4)) {

            cliente.setNome(txtNome.getText());
            cliente.setCPF(txtCpf.getText());
            cliente.setNumTelefone(txtTelefone.getText());
            cliente.setDataNasc(txtData.getText());
            cliente.setEndereco(txtEndereco.getText());
            cliente.setNumeroCasa(txtN.getText());
            cliente.setCidade(txtCidade.getText());

            cliente.setEstado(txtUf.getText());

            dao.grava(cliente);
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao inserir o Cliente." + txtNome);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

