    public void readJtableMor() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        ClienteDao dao = new ClienteDao();

        dao.lista().forEach((cli) -> {
            modelo.addRow(new Object[]{
                cli.getNome(),
                cli.getCPF(),
                cli.getNumTelefone(),
                cli.getDataNasc(),
                cli.getEndereco(),
                cli.getNumeroCasa(),
                cli.getCidade(),
                cli.getEstado()
            });
        });
    }

